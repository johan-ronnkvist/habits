// Utility function to get theme by ID
export const getTheme = (themeId) => {
  if (themeId === 'default') return defaultTheme
  return themes[themeId] || defaultTheme
}

// Utility function to get all available themes including default
export const getAllThemes = () => {
  return [defaultTheme, ...Object.values(themes)]
}

// Apply theme to CSS variables
export const applyTheme = (theme) => {
  const root = document.documentElement
  
  // Apply all color variables
  Object.entries(theme.colors).forEach(([key, value]) => {
    root.style.setProperty(`--color-${key}`, value)
  })
  
  // Save to localStorage
  localStorage.setItem('selectedTheme', theme.id)
}

// Get saved theme from localStorage
export const getSavedTheme = () => {
  const savedThemeId = localStorage.getItem('selectedTheme')
  return getTheme(savedThemeId)
}

// Initialize theme on app load
export const initializeTheme = () => {
  const savedTheme = getSavedTheme()
  applyTheme(savedTheme)
  return savedTheme
}